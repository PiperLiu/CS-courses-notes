//
// Serial crawler
//
// 串行爬取
// 为什么直接传入一个 map ？因为 map 本身就是一个指针
func Serial(url string, fetcher Fetcher, fetched map[string]bool) {
	if fetched[url] {
		return
	}
	fetched[url] = true
	urls, err := fetcher.Fetch(url)
	if err != nil {
		return
	}
	for _, u := range urls {
		Serial(u, fetcher, fetched)
	}
	return
}

//
// Concurrent crawler with shared state and Mutex
//
// 使用共享的 state 和互斥锁来做
type fetchState struct {
	mu      sync.Mutex
	fetched map[string]bool
}

func ConcurrentMutex(url string, fetcher Fetcher, f *fetchState) {
	// 这里的封装同样是把 mutex 和数据封装在一个 struct 里
	f.mu.Lock()
	already := f.fetched[url]
	f.fetched[url] = true
	f.mu.Unlock()

	if already {
		return
	}

	urls, err := fetcher.Fetch(url)
	if err != nil {
		return
	}
	// 这里对 WaitGroup 的应用堪称经典
	// 对于每个新任务，我先 done.Add(1)
	// 完成了再 done.done
	// 这样做到等待每个任务完成
	var done sync.WaitGroup
	for _, u := range urls {
		done.Add(1)
        u2 := u
		go func() {
			defer done.Done()
			ConcurrentMutex(u2, fetcher, f)
		}()
		//go func(u string) {
		//	defer done.Done()
		//	ConcurrentMutex(u, fetcher, f)
		//}(u)
	}
	done.Wait()
	return
}

func makeState() *fetchState {
	f := &fetchState{}
	f.fetched = make(map[string]bool)
	return f
}

//
// Concurrent crawler with channels
//
// 使用 channel 来做， master 持续接收 worker 传来的 url ，并在自己的作用域内维护 map
func worker(url string, ch chan []string, fetcher Fetcher) {
	urls, err := fetcher.Fetch(url)
	if err != nil {
		ch <- []string{}
	} else {
		ch <- urls
	}
}

func master(ch chan []string, fetcher Fetcher) {
	n := 1
	fetched := make(map[string]bool)
	for urls := range ch {  // urls 为 ch 传进来的 urls
		for _, u := range urls {
			if fetched[u] == false {
				fetched[u] = true
				n += 1  // 如果有新 url ，那么 n += 1
				go worker(u, ch, fetcher)
			}
		}
		n -= 1  // 已经给 1 个 url 分配过 channel 了
		if n == 0 {  // 本 urls 中所有的新 url 已经分配过 worker
			// 不必等待每个 worker fetcher 完再结束
			// 如果确定没有新 urls 了，那么跳出结束 ch 即可
			break
		}
	}
}

func ConcurrentChannel(url string, fetcher Fetcher) {
	ch := make(chan []string)
	go func() {
		ch <- []string{url}
	}()
	master(ch, fetcher)
}

